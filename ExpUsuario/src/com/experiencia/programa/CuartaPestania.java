    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        String densidadComentarios = (String) jComboBoxComentarios.getSelectedItem();
        switch(densidadComentarios){
            case "Buena": puntosComentarios += Puntos.BUENA.getPuntos();break;
            case "Regular": puntosComentarios += Puntos.REGULAR.getPuntos();break;
            case "Mala": puntosComentarios += Puntos.MALA.getPuntos();break;
        }
        String complejidadCodigo = (String) jComboBoxComplejidad.getSelectedItem();
        switch(complejidadCodigo){
            case "Baja": puntosComplejidad += Puntos.BUENA.getPuntos();break;
            case "Regular": puntosComplejidad += Puntos.REGULAR.getPuntos();break;
            case "Alta": puntosComplejidad += Puntos.MALA.getPuntos();break;
        }
        String fallas = (String) jComboBoxFallas.getSelectedItem();
        switch(fallas){
            case "Entre 0 y 1": puntosFallas += Puntos.BUENA.getPuntos();break;
            case "Entre 2 y 4": puntosFallas += Puntos.REGULAR.getPuntos();break;
            case "5 o m√°s": puntosFallas += Puntos.MALA.getPuntos();break;
        }
        asignarPuntos();
        QuintaPestania usabilidad = new QuintaPestania();
        usabilidad.requestFocusInWindow();
        usabilidad.setPuntuacionGeneral(puntuacionGeneral);
        usabilidad.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

