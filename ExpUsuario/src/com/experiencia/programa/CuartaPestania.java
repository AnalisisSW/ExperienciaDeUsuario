    private void asignarPuntos() {
        puntuacionGeneral.getMantenibilidad().replace(Mantenibilidad.ANALIZADO.getAtributo(), puntosComentarios);
        puntuacionGeneral.getMantenibilidad().replace(Mantenibilidad.CAMBIADO.getAtributo(), puntosComplejidad);
        puntuacionGeneral.getMantenibilidad().replace(Mantenibilidad.ESTABILIDAD.getAtributo(), puntosFallas);
        puntuacionGeneral.sumar(puntosComentarios);
        puntuacionGeneral.sumar(puntosComplejidad);
        puntuacionGeneral.sumar(puntosFallas);
    }
