    private void asignarPuntos() {
        puntuacionGeneral.getEficiencia().replace(Eficiencia.RECURSOS.getAtributo(), puntosRecursos);
        puntuacionGeneral.getEficiencia().replace(Eficiencia.COMPORTAMIENTO.getAtributo(), puntosComportamiento);
        puntuacionGeneral.sumar(puntosRecursos);
        puntuacionGeneral.sumar(puntosComportamiento);
    }
