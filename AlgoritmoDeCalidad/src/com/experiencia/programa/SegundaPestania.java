    private void jButtonSiguiente1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguiente1ActionPerformed
        String calificacionRecursos = (String)jComboBoxRecursos.getSelectedItem();
        switch(calificacionRecursos){
            case "Buena": puntosRecursos += Puntos.BUENA.getPuntos();break;
            case "Regular": puntosRecursos += Puntos.REGULAR.getPuntos();break;
            case "Mala": puntosRecursos += Puntos.MALA.getPuntos();break;
        }
        String calificacionComportamiento = (String) jComboBoxTiempo.getSelectedItem();
        switch(calificacionComportamiento){
            case "Bueno": puntosComportamiento += Puntos.BUENA.getPuntos();break;
            case "Regular": puntosComportamiento += Puntos.REGULAR.getPuntos();break;
            case "Malo": puntosComportamiento += Puntos.MALA.getPuntos();break;
        }
        asignarPuntos();
        TercerPestania fiabilidad = new TercerPestania();
        fiabilidad.requestFocusInWindow();
        fiabilidad.setPuntuacionGeneral(puntuacionGeneral);
        fiabilidad.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonSiguiente1ActionPerformed

