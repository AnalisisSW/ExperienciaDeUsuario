    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        if(jRadioAyudaFalse.isSelected() && jRadioManualFalse.isSelected()){
            puntosCapEntendido += Puntos.MALA.getPuntos();
        } else if(jRadioAyudaTrue.isSelected() && jRadioManualTrue.isSelected()) {
            puntosCapEntendido += Puntos.BUENA.getPuntos();
        } else {
            puntosCapEntendido += Puntos.REGULAR.getPuntos();
        }
        String capOperado = (String) jComboBoxAsistencia.getSelectedItem();
        switch(capOperado) {
            case "Si": puntosCapOperado += Puntos.BUENA.getPuntos();break;
            case "Medianamente": puntosCapOperado += Puntos.REGULAR.getPuntos();break;
            case "No": puntosCapOperado += Puntos.MALA.getPuntos();break;
        }
        String pasos = (String) jComboBoxCantPasos.getSelectedItem();
        switch(pasos){
            case "1 o 2 pasos": puntosCapAtractivo += Puntos.BUENA.getPuntos();break;
            case "Entre 3 y 5 pasos": puntosCapAtractivo += Puntos.REGULAR.getPuntos();break;
            case "6 o más pasos": puntosCapAtractivo += Puntos.MALA.getPuntos();break;
        }
        asignarPuntos();
        SextaPestania portabilidad = new SextaPestania();
        portabilidad.requestFocusInWindow();
        portabilidad.setPuntuacionGeneral(puntuacionGeneral);
        portabilidad.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

