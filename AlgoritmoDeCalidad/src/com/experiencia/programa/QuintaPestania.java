    private void asignarPuntos() {
        puntuacionGeneral.getUsabilidad().replace(Usabilidad.CAPENTENDIDO.getAtributo(), puntosCapEntendido);
        puntuacionGeneral.getUsabilidad().replace(Usabilidad.CAPOPERADO.getAtributo(), puntosCapOperado);
        puntuacionGeneral.getUsabilidad().replace(Usabilidad.CAPATRACTIVO.getAtributo(), puntosCapAtractivo);
        puntuacionGeneral.sumar(puntosCapEntendido);
        puntuacionGeneral.sumar(puntosCapOperado);
        puntuacionGeneral.sumar(puntosCapAtractivo);
    }
