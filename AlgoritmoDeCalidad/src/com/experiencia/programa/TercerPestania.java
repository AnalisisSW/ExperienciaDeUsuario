    private void asignarPuntos() {
        puntuacionGeneral.getFiabilidad().replace(Fiabilidad.TOLFALLOS.getAtributo(), puntosToleranciaFallos);
        puntuacionGeneral.getFiabilidad().replace(Fiabilidad.RECUPERACION.getAtributo(), puntosRecuperacion);
        puntuacionGeneral.sumar(puntosToleranciaFallos);
        puntuacionGeneral.sumar(puntosRecuperacion);
    }
