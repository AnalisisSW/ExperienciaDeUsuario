    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        if(jRadioRecuperaFalse.isSelected() && jRadioLogFalse.isSelected()){
            puntosToleranciaFallos += Puntos.MALA.getPuntos();
        } else if(jRadioRecuperaTrue.isSelected() && jRadioLogTrue.isSelected()) {
            puntosToleranciaFallos += Puntos.BUENA.getPuntos();
        } else {
            puntosToleranciaFallos += Puntos.REGULAR.getPuntos();
        }
        if(jRadioReanudaFalse.isSelected() && jRadioEstadoFalse.isSelected()){
            puntosRecuperacion += Puntos.MALA.getPuntos();
        } else if(jRadioReanudaTrue.isSelected() && jRadioEstadoTrue.isSelected()){
            puntosRecuperacion += Puntos.BUENA.getPuntos();
        } else {
            puntosRecuperacion += Puntos.REGULAR.getPuntos();
        }
        asignarPuntos();
        CuartaPestania mantenibilidad = new CuartaPestania();
        mantenibilidad.requestFocusInWindow();
        mantenibilidad.setPuntuacionGeneral(puntuacionGeneral);
        mantenibilidad.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

